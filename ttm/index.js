document.onkeyup = (event) => {
    switch (event.key) {
        case 'A': ; break; //--> New Scheduled Item
        case 'a': ; break; //--> New Item
        case 'n': ; break; //--> New Column
        case 'e': ; break; //--> Edit Item
        case 'x': ; break; //--> Delete Item
        case 'j': ; break; //--> Item Down
        case 'k': ; break; //--> Item Up
        case 'h': ; break; //--> Column Left
        case 'l': ; break; //--> Column Right
    }
}